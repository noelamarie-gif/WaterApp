    private void displayWaterQuality() {
        String location = source.getLocation().toLowerCase();

        String pHLevel;
        String contaminant;
        String quality;

        if (location.contains("river")) {
            pHLevel = "7.2";
            contaminant = "Nitrates";
            quality = "Good";
        } else if (location.contains("lake")) {
            pHLevel = "3.4";
            contaminant = "Industrial waste";
            quality = "Bad";
        } else {
            pHLevel = "7.0";
            contaminant = "Unknown";
            quality = "Pending Analysis";
        }

        pHTF.setText(pHLevel);
        contaminantTF.setText(contaminant);
        qualityTF.setText(quality);

        pHTF.setEditable(false);
        contaminantTF.setEditable(false);
        qualityTF.setEditable(false);

    }
    

