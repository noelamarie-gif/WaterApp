    private void createReportBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createReportBTNActionPerformed
        // TODO add your handling code here:                                                                                                                                    
    try {
        String contaminant = contaminantTF.getText().trim();
        String qualityGrade = qualityTF.getText().trim();
        double pHLevel = Double.parseDouble(pHTF.getText().trim());

        WaterQualityModel model = new WaterQualityModel(contaminant, qualityGrade, pHLevel);

        javax.swing.JOptionPane.showMessageDialog(this,
            "Water Quality Report Created!\n\n" + model.toString(),
            "Report Saved",
            javax.swing.JOptionPane.INFORMATION_MESSAGE);

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Please enter a valid numeric value for pH Level.",
            "Invalid Input",
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }




    }//GEN-LAST:event_createReportBTNActionPerformed

